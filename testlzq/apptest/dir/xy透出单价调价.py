    # d = x*n  # 前p升单价
    dj = (200-a-c+t)*n/200
    y = 20  # 最高直降Y元/升
    # y = ('%.2f' % i)

    #
    sum1 = (v-x)*h+w  # 易加油满两百需付金额
    sum2 = 200-a  # 竞品满两百需付金额
    #
    if(dj < m) :
        print("活动价%s<结算价%s" % (dj, m), "不调价!!")
