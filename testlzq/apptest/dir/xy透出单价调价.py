        print("活动价%s>=结算价%s" % (dj, m), "可以调价!!")
        print("最高直降%s元" % (y))
        if(x<y) :
            print("直降金额%s元/升,活动价为:%s" % (x, (v-x)))
        else:
            print("直降金额%s元/升,活动价为:%s" % (y, (v-y)))
        print("%s+%s=%s" % (sum1, c, sum2))

